# Задание 1.
# Найдите максимальное и минимальное значения в словаре:
# d = {'a': 10, 'b': 20, 'c': 30}

d = {'a': 10, 'b': 20, 'c': 30}

print("Исходный словарь:", d)

max_value = max(d.values())
min_value = min(d.values())

print("Максимальное значение:", max_value)
print("Минимальное значение:", min_value)
# Задание 2.
# Дана строка:
# 'апельсины 45 991 63 100 12 яблоки 13 47 26 0 16'
# Преобразовать информацию из строки в словарь.
# С использованием функций найти максимальные продажи
# по каждому виду продукции и вывести результат на экран.

data = 'апельсины 45 991 63 100 12 яблоки 13 47 26 0 16'

print("Исходная строка:")
print(data)

items = data.split()

sales = {
    items[0]: list(map(int, items[1:6])),
    items[6]: list(map(int, items[7:12]))
}

print("\nСловарь продаж:")
print(sales)

max_oranges = max(sales['апельсины'])
max_apples = max(sales['яблоки'])

print("\nМаксимальные продажи:")
print("Апельсины:", max_oranges, "кг")
print("Яблоки:", max_apples, "кг")
